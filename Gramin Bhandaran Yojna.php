  <h2>Gramin Bhandaran Yojna (ग्रामीण भंडारन योजना)</h2><br />
  <h5>Objective of this Scheme:</h5>
  <p>
  <ul>
    <li>Create scientific storage capacity with allied facilities in rural areas.</li>
    <li>To meet the requirements of farmers for storing farm produce, processed farm produce and agricultural
      inputs.</li>
    <li>Promotion of grading, standardization and quality control of agricultural produce to improve their
      marketability.</li>
    <li>Prevent distress sale immediately after harvest by providing the facility of pledge financing and marketing
      credit by strengthening agricultural marketing infrastructure in the country.</li>
  </ul>
  </p>
  <h5>इस योजना का उद्देश्य:</h5>
  <p>
  <ul>
    <li>ग्रामीण क्षेत्रों में संबद्ध सुविधाओं के साथ वैज्ञानिक भंडारण क्षमता का सृजन करना।</li>
    <li>कृषि उपज, प्रसंस्कृत कृषि उपज और कृषि आदानों के भंडारण के लिए किसानों की आवश्यकताओं को पूरा करना।</li>
    <li>कृषि उत्पादों की विपणन क्षमता में सुधार के लिए ग्रेडिंग, मानकीकरण और गुणवत्ता नियंत्रण को बढ़ावा देना।</li>
    <li>देश में कृषि विपणन बुनियादी ढांचे को मजबूत करके गिरवी वित्तपोषण और विपणन ऋण की सुविधा प्रदान करके फसल के
      तुरंत बाद संकट बिक्री को रोकें।</li>
  </ul>
  </p>

