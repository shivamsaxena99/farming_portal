  <h2>Paramparagat Krishi Vikas Yojana (परम्परागत कृषि विकास योजना)</h2><br />
  <p>Parampragat Krishi Vikas Yojana (PKVY) is one of the important government schemes which encourages farmers for traditional and organic farming in India. Under the scheme, the Government of India provides a financial assistance to the farmers of Rs 50,000 per hectare every three years for organic inputs, certification, labelling, packaging, transportation and marketing of organic produce. The scheme focuses on reducing the ill effects of overuse of fertilisers and agrochemicals by promoting organic manures, bio-fertilisers and bio-pesticides. It helps improve the soil fertility by improving organic carbon in the soil which results in enhancing moisture holding capacity in the field too.</P><br /><br />
  <p>परम्परागत कृषि विकास योजना (पीकेवीवाई) महत्वपूर्ण सरकारी योजनाओं में से एक है जो भारत में किसानों को पारंपरिक और जैविक खेती के लिए प्रोत्साहित करती है। इस योजना के तहत, भारत सरकार जैविक उत्पादों के जैविक आदानों, प्रमाणीकरण, लेबलिंग, पैकेजिंग, परिवहन और विपणन के लिए किसानों को हर तीन साल में 50,000 रुपये प्रति हेक्टेयर की वित्तीय सहायता प्रदान करती है। यह योजना जैविक खाद, जैव उर्वरक और जैव कीटनाशकों को बढ़ावा देकर उर्वरकों और कृषि रसायनों के अति प्रयोग के दुष्प्रभावों को कम करने पर केंद्रित है। यह मिट्टी में कार्बनिक कार्बन में सुधार करके मिट्टी की उर्वरता में सुधार करने में मदद करता है जिसके परिणामस्वरूप खेत में नमी धारण करने की क्षमता भी बढ़ती है।</p>

