  <h2>Pradhan Mantri Fasal Bima Yojana (प्रधानमंत्री फसल बीमा योजना)</h2><br />
  <p>The extreme weather conditions such as droughts, floods and hailstorms cause crop failures and financial loss to the farmers every year. And therefore, to save them from these ill effects, the Government, by merging all previous crop insurance schemes, launched Pradhan Mantri Fasal Bima Yojana (PMFBY) from Kharif 2016 season with the aim to support production in agriculture by providing an affordable crop insurance system.<br /><br />Central government has made Aadhaar mandatory for availing crop insurance from Kharif 2017 season. The scheme has been restructured with states given option to determine scale of finance at district level as per average yield.</P><br /><br />
  <p>सूखे, बाढ़ और ओलावृष्टि जैसी चरम मौसम की स्थिति हर साल किसानों को फसल की विफलता और वित्तीय नुकसान का कारण बनती है। और इसलिए, उन्हें इन दुष्प्रभावों से बचाने के लिए, सरकार ने, पिछली सभी फसल बीमा योजनाओं को मिलाकर, एक किफायती फसल बीमा प्रणाली प्रदान करके कृषि में उत्पादन का समर्थन करने के उद्देश्य से खरीफ 2016 सीजन से प्रधान मंत्री फसल बीमा योजना (पीएमएफबीवाई) शुरू की।<br /><br />केंद्र सरकार ने खरीफ 2017 सीजन से फसल बीमा लेने के लिए आधार अनिवार्य कर दिया है। इस योजना का पुनर्गठन किया गया है, जिसमें राज्यों को औसत उपज के अनुसार जिला स्तर पर वित्त के पैमाने को निर्धारित करने का विकल्प दिया गया है।</p>

