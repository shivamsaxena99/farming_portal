  <h2>Pradhan Mantri Krishi Sinchai Yojana (प्रधानमंत्री कृषि सिंचाई योजना)</h2><br />
  <p>The Government of India has structured Pradhan Mantri Krishi Sinchayee Yojana (PMKSY) with the vision to extend the coverage of irrigation and improving water use efficiency in a focused manner. PMKSY focuses end to end solution on source creation, distribution, management, field application and extension activities. The Cabinet Committee on Economic Affairs had approved the irrigation scheme on July 1,, 2015.<br /><br />The Centre has structured the scheme with merging previous irrigation and water management programmes such as Accelerated Irrigation Benefit Programme (AIBP), Integrated Watershed Management Programme (IWMP) and On Farm Water Management (OFWM) scheme. The Centre has approved Rs. 50,000 crore for five years for the implementation of the flagship irrigation scheme across India.</P><br /><br />
  <p>भारत सरकार ने प्रधान मंत्री कृषि सिंचाई योजना (पीएमकेएसवाई) को सिंचाई के कवरेज का विस्तार करने और एक केंद्रित तरीके से जल उपयोग दक्षता में सुधार करने की दृष्टि से संरचित किया है। पीएमकेएसवाई स्रोत निर्माण, वितरण, प्रबंधन, फील्ड एप्लिकेशन और विस्तार गतिविधियों पर एंड टू एंड समाधान पर ध्यान केंद्रित करता है। आर्थिक मामलों की मंत्रिमंडलीय समिति ने 1 जुलाई, 2015 को सिंचाई योजना को मंजूरी दी थी।<br /><br />केंद्र ने पिछले सिंचाई और जल प्रबंधन कार्यक्रमों जैसे त्वरित सिंचाई लाभ कार्यक्रम (AIBP), एकीकृत वाटरशेड प्रबंधन कार्यक्रम (IWMP) और ऑन फार्म वाटर मैनेजमेंट (OFWM) योजना को मिलाकर योजना तैयार की है। केंद्र ने रु. भारत भर में प्रमुख सिंचाई योजना के कार्यान्वयन के लिए पांच साल के लिए 50,000 करोड़।</p>

